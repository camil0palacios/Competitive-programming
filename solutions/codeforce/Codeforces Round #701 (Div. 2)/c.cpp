int main() { 
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int t;
    cin >> t;
    while(t--) {
        int x, y;
        cin >> x >> y;
        ll ans = 0;
        for(int i = 1; i*i <= x; i++) {
            int bl = i+1, br = min(x/i-1, y);
            if(bl <= br) ans += br - bl + 1;
        }
        cout << ans << endl;
    }
    return 0; 
}